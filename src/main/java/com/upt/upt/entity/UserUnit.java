package com.upt.upt.entity;

import jakarta.persistence.*;
import java.util.HashSet;
import java.util.Set;

/**
 * User class represents a user entity to be mapped to the database.
 */
@Entity
@Inheritance(strategy = InheritanceType.SINGLE_TABLE) // Define o tipo de heran√ßa
@Table(name = "user_unit") // Define a tabela na classe raiz
public abstract class UserUnit {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Column(name = "user_id")
    private Long id;

    @Column(name = "user_name", nullable = false)
    private String name;

    @Column(name = "user_username", nullable = false, unique = true)
    private String username;

    @Column(name = "user_password", nullable = false)
    private String password;

    // Construtores, getters e setters

    public UserUnit() {}

    public UserUnit(Long id, String name, String username, String password) {
        this.id = id;
        this.name = name;
        this.username = username;
        this.password = password;
    }

    // Getters e setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

}
